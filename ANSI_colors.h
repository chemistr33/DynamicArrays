#ifndef _ANSI_COLORS_H__
#define _ANSI_COLORS_H__

// Regular Colors
#define ANSI_BLACK "\x1b[30m"
#define ANSI_RED "\x1b[31m"
#define ANSI_GREEN "\x1b[32m"
#define ANSI_YELLOW "\x1b[33m"
#define ANSI_BLUE "\x1b[34m"
#define ANSI_MAGENTA "\x1b[35m"
#define ANSI_CYAN "\x1b[36m"
#define ANSI_WHITE "\x1b[37m"

// Bold Colors
#define ANSI_BOLD_BLACK "\x1b[1;30m"
#define ANSI_BOLD_RED "\x1b[1;31m"
#define ANSI_BOLD_GREEN "\x1b[1;32m"
#define ANSI_BOLD_YELLOW "\x1b[1;33m"
#define ANSI_BOLD_BLUE "\x1b[1;34m"
#define ANSI_BOLD_MAGENTA "\x1b[1;35m"
#define ANSI_BOLD_CYAN "\x1b[1;36m"
#define ANSI_BOLD_WHITE "\x1b[1;37m"

// Underline Colors
#define ANSI_UNDERLINE_BLACK "\x1b[4;30m"
#define ANSI_UNDERLINE_RED "\x1b[4;31m"
#define ANSI_UNDERLINE_GREEN "\x1b[4;32m"
#define ANSI_UNDERLINE_YELLOW "\x1b[4;33m"
#define ANSI_UNDERLINE_BLUE "\x1b[4;34m"
#define ANSI_UNDERLINE_MAGENTA "\x1b[4;35m"
#define ANSI_UNDERLINE_CYAN "\x1b[4;36m"
#define ANSI_UNDERLINE_WHITE "\x1b[4;37m"

// Background Colors
#define ANSI_BG_BLACK "\x1b[40m"
#define ANSI_BG_RED "\x1b[41m"
#define ANSI_BG_GREEN "\x1b[42m"
#define ANSI_BG_YELLOW "\x1b[43m"
#define ANSI_BG_BLUE "\x1b[44m"
#define ANSI_BG_MAGENTA "\x1b[45m"
#define ANSI_BG_CYAN "\x1b[46m"
#define ANSI_BG_WHITE "\x1b[47m"

// High Intensity Colors
#define ANSI_HIGH_INTENSITY_BLACK "\x1b[0;90m"
#define ANSI_HIGH_INTENSITY_RED "\x1b[0;91m"
#define ANSI_HIGH_INTENSITY_GREEN "\x1b[0;92m"
#define ANSI_HIGH_INTENSITY_YELLOW "\x1b[0;93m"
#define ANSI_HIGH_INTENSITY_BLUE "\x1b[0;94m"
#define ANSI_HIGH_INTENSITY_MAGENTA "\x1b[0;95m"
#define ANSI_HIGH_INTENSITY_CYAN "\x1b[0;96m"
#define ANSI_HIGH_INTENSITY_WHITE "\x1b[0;97m"

// Bold High Intensity Colors
#define ANSI_BOLD_HIGH_INTENSITY_BLACK "\x1b[1;90m"
#define ANSI_BOLD_HIGH_INTENSITY_RED "\x1b[1;91m"
#define ANSI_BOLD_HIGH_INTENSITY_GREEN "\x1b[1;92m"
#define ANSI_BOLD_HIGH_INTENSITY_YELLOW "\x1b[1;93m"
#define ANSI_BOLD_HIGH_INTENSITY_BLUE "\x1b[1;94m"
#define ANSI_BOLD_HIGH_INTENSITY_MAGENTA "\x1b[1;95m"
#define ANSI_BOLD_HIGH_INTENSITY_CYAN "\x1b[1;96m"
#define ANSI_BOLD_HIGH_INTENSITY_WHITE "\x1b[1;97m"

// High Intensity Background Colors
#define ANSI_HIGH_INTENSITY_BG_BLACK "\x1b[0;100m"
#define ANSI_HIGH_INTENSITY_BG_RED "\x1b[0;101m"
#define ANSI_HIGH_INTENSITY_BG_GREEN "\x1b[0;102m"
#define ANSI_HIGH_INTENSITY_BG_YELLOW "\x1b[0;103m"
#define ANSI_HIGH_INTENSITY_BG_BLUE "\x1b[0;104m"
#define ANSI_HIGH_INTENSITY_BG_MAGENTA "\x1b[0;105m"
#define ANSI_HIGH_INTENSITY_BG_CYAN "\x1b[0;106m"
#define ANSI_HIGH_INTENSITY_BG_WHITE "\x1b[0;107m"

// Reset
#define ANSI_RESET "\x1b[0m"

// Blinking text
#define ANSI_BLINK "\x1b[5m"

#endif // _ANSI_COLORS_H__
